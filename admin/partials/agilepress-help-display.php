<h3>Quick Start Guide</h3>
<p>Kanban may seem like a strange word (unless you happen to speak Japanese!) but it’s really just another word for a graphical to-do list.  The <strong>AgilePress</strong> Kanban board is great for personal projects, from remodeling your house to planning a party or any number of other multi-step endeavors.</p>
<a href="https://agilepress.io/documentation/quick-start-guide/" target="_blank">Get Started</a>
<hr />
<br />
<h3>AgilePress Boards</h3>
<p>Boards are the forward-facing element of <strong>AgilePress</strong>; it’s where the sticky notes live.  Depending on a user’s role, s/he might have full use of the boards, only be able to view them, or not be able to see them at all.  Ideally, you can use view-access to allow visibility into your project, while those with higher levels of access can use the boards without ever going into the WordPress admin screens.</p>
<a href="https://agilepress.io/documentation/agilepress-boards/" target="_blank">AgilePress Boards</a>
<hr />
<br />
<h3>AgilePress Admin</h3>
<p><strong>AgilePress</strong> uses the familiar WordPress admin screens for the bulk of its setup.  Although most <strong>AgilePress</strong> functions can be done from the boards themselves, the Admin post screens are where a new <strong>AgilePress</strong> administrator should start.</p>
<a href="https://agilepress.io/documentation/agilepress-admin/" target="_blank">AgilePress Admin</a>
<hr />
<br />
<h3>Roles and Permissions</h3>
<p>The roles and their associated permissions used by <strong>AgilePress</strong> are very important to proper functionality.  There are four roles: <strong>AgilePress Admin</strong>, <strong>AgilePress Developer</strong>, <strong>AgilePress User</strong>, and <strong>AgilePress Viewer<strong>.  There are also a number of new capabilities.</p>
<a href="https://agilepress.io/documentation/roles-and-permissions/" target="_blank">Roles and Permissions</a>
